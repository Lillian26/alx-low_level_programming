#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - Determine if an number is positive, negative or Zero.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	int n;
	int m;

	for (n = 48; n <= 58; n++)
	{
		putchar(n);
	}
	for (m = 97; m <= 103; m++)
	{
		putchar(m);
	}
	return (0);
}
